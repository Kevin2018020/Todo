    public void FiltrarCategoria(){
    //poner el combo variable Cat
    int Cat=0;
    
    String Categorias= cmbBCategoriaProd.getSelectedItem().toString();
    
    if(Categorias.equals("Categoria 0")){
        Cat=0;
    }
    else if(Categorias.equals("Categoria 1")){
        Cat=1;
    }
    else if(Categorias.equals("Categoria 2")){
        Cat=2;
    }
    
    String titulos[]={"IdProducto","Nombre","Categoría","Cantidad"};
    String registro[]=new String[4];

    
    String sql="SELECT * FROM productos INNER JOIN stock ON productos.IdProducto = stock.IdProducto and productos.Categoria = '"+Cat+"'";
     m=new DefaultTableModel(null,titulos);
    try{
    Statement st=cn.createStatement();
    ResultSet r1=st.executeQuery(sql);
    while(r1.next() ){
    registro[0]=r1.getString("IdProducto");
    registro[1]=r1.getString("Nombre");
    registro[2]=r1.getString("Categoria");
    registro[3]=r1.getString("Cantidad");
    m.addRow(registro);
    }
    TablaRes.setModel(m);
    }catch(SQLException ex){
    JOptionPane.showMessageDialog(null, ex);
    }
    }

   public void clock(){
   
     jTabbedPane1.setSelectedIndex(0);
   
    String Nombre= ""; 
    String Id= ""; 
    int cantidad= 0;
    int StockActual= 0;
                                            
    
    
    
     try
        { String sql="select * from productos where IdProducto='"+ txtBIdProd.getText()+"'";
            PreparedStatement pes=cn.prepareStatement(sql);
            ResultSet r=pes.executeQuery();
            while(r.next())
            {
                Nombre=r.getString("Nombre");
                Id = r.getString("IdProducto");
                txtNombreProd.setText(Nombre); 
                txtIdProducto.setText(Id);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    
     try
        {
            String sql="select * from stock where IdProducto='"+Id+"'";
            PreparedStatement pes=cn.prepareStatement(sql);
            ResultSet r=pes.executeQuery();
            while(r.next())
            {
                cantidad=r.getInt("Cantidad");
                txtStockActual.setText(cantidad + "");
               
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        
    
       
}
